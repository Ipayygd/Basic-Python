# Operasi Komparasi (>, <, ==, !=, >=, <=, is, is not)
# Setiap hasil operasi komparasi akan bernilai boolean, (True/False)

a = 10
b = 5

# Lebih besar (>)
hasil = a > b
print(a, ">", b, "=", hasil)

# Lebih kecil (<)
hasil = a < b
print(a, "<", b, "=", hasil)

# Sama dengan (==)
hasil = a == b
print(a, "==", b, "=", hasil)

# Tidak sama dengan (!=)
hasil = a != b
print(a, "!=", b, "=", hasil)

# Lebih besar sama dengan (>=)
hasil = a >= b
print(a, ">=", b, "=", hasil)

# Lebih kecil sama dengan (<=)
hasil = a <= b
print(a, "<=", b, "=", hasil)