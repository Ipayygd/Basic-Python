# Cara-cara membuat String

data = "Ini adalah string"
print(data)
print(type(data))

# 1. Dengan menggunakan single quote (')
data = 'Menggunakan single quote'
print(data)

# 2. Dengan menggunakan double quote (")
data = "Menggunakan double quote"
print(data)

# 3. Dengan menggunakan triple quote (""")
data = """Menggunakan triple quote
dengan baris baru"""
print(data)

# 4. Dengan menggunakan backslash (\)
data = "Hari ini adalah Jum\'at"
print(data)

# 5. String Literal
print("C:\new Folder") # Salah
print(r"C:\New Folder") # Benar (Menggunakan raw string)

# 6. String Multiline
data = """Menggunakan,
Multiline String"""
print(data)


